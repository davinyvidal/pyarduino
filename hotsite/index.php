           <p>Olá, PyLadies! Esperamos que estejam todas bem!<p>
            Dando início às nossas lives em quarentena, convidamos você para participar do Workshop Python com Arduíno.</p>
            <p>Primeiramente, você sabe o que é Arduíno?</p>
            <p>Arduíno é uma plataforma de prototipagem eletrônica de código aberto. Ou seja, um software disponível gratuitamente para download. Atualmente, bastante usado na robótica.</p>
            <p>E, como podemos desenvolver códigos Python fazendo a interface com Arduíno?</p>
            <p>Este é o objetivo do nosso workshop! Você vai ter a oportunidade de colocar a mão na massa e aprender na prática esta relação entre Python e Arduino.</p>
